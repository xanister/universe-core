/**
 * Entity IDs follow a strict prefix convention for type safety and debugging:
 *
 * - **CHAR_** - Character entities (e.g., `CHAR_aldric`, `CHAR_veteran_tom`)
 * - **PLACE_** - Place entities (e.g., `PLACE_tavern`, `PLACE_market_square`)
 * - **OBJ_** - Object entities for scenery/obstacles/exits (e.g., `OBJ_oak_tree`, `OBJ_exit_tavern_door`)
 *
 * ID Format:
 * - Prefix is UPPERCASE with underscore separator
 * - Suffix is lowercase with underscores (slug format)
 * - Example: `CHAR_mysterious_stranger`, `PLACE_ancient_ruins`
 *
 * Generation:
 * - New entity IDs are generated by services/core/universe-store.ts
 * - Characters: `generateCharacterId(name)` → `CHAR_{slugified_name}`
 * - Places: `generatePlaceId(name)` → `PLACE_{slugified_name}`
 * - Objects/Exits: Manual creation, typically `OBJ_{descriptive_name}` or `OBJ_exit_{name}`
 *
 * Validation helpers:
 * - `id.startsWith('CHAR_')` to check if entity is a character
 * - `id.startsWith('PLACE_')` to check if entity is a place
 * - `id.startsWith('OBJ_')` to check if entity is an object
 */

/**
 * Distance units from small to cosmic scale.
 * Used for place dimensions and position coordinates.
 */
export type DistanceUnit =
  | 'feet' // Indoor spaces, small outdoor areas
  | 'meters' // Metric alternative
  | 'miles' // Regions, wilderness, seas
  | 'kilometers' // Metric alternative
  | 'au' // Astronomical units (solar system scale)
  | 'lightyears'; // Interstellar/galactic scale

/** Const array for schema enum usage - derived from DistanceUnit type. */
export const DISTANCE_UNITS = [
  'feet',
  'meters',
  'miles',
  'kilometers',
  'au',
  'lightyears',
] as const;

/**
 * Unified position and spatial data for all entities.
 * Coordinates are relative to the parent place, using the parent's scale unit.
 * All entities MUST have valid x/y coordinates - use center of parent if unknown.
 *
 * Key insight: Position coordinates use the PARENT's scale, not the entity's own scale.
 * A tavern at { x: 0.75, y: 1.15, parent: "PLACE_city" } is 0.75 miles from city origin
 * if the city's scale is 'miles', even though the tavern's own scale is 'feet'.
 *
 * Place dimensions (two distinct concepts):
 * - **width, height**: Size when the place is rendered as an object in its parent (from slot at
 *   creation, or from layout for root). Objects use width/height for collision bounds (in parent's scale).
 * - **innerWidth, innerHeight**: Playable area inside the place (from layout). Used for game room
 *   bounds, character movement, object placement.
 */
export interface Position {
  /** X coordinate relative to parent (in parent's scale unit) */
  x: number;
  /** Y coordinate relative to parent (in parent's scale unit) */
  y: number;
  /** Parent place ID, null only for root (cosmos) */
  parent: string | null;

  // ---- Spatial Dimensions ----

  /** For places: size when rendered as object in parent. For objects: collision bounds in parent's scale. */
  width: number;
  /** For places: size when rendered as object in parent. For objects: collision bounds in parent's scale. */
  height: number;

  /** Place only: playable area inside the place (from layout). */
  innerWidth?: number;
  /** Place only: playable area inside the place (from layout). */
  innerHeight?: number;
}

/**
 * Constrained relationship types between characters.
 * Prevents hallucinated types like "potential_childhood_acquaintance".
 */
export const RELATIONSHIP_TYPES = [
  'stranger',
  'acquaintance',
  'colleague',
  'friend',
  'rival',
  'enemy',
  'family',
  'romantic',
  'mentor',
  'subordinate',
  'superior',
] as const;

export type RelationshipType = (typeof RELATIONSHIP_TYPES)[number];

/** Runtime type guard for RelationshipType. */
export function isRelationshipType(value: string): value is RelationshipType {
  return (RELATIONSHIP_TYPES as readonly string[]).includes(value);
}

/**
 * A structured relationship between a character and another entity (character or place).
 *
 * For characters: tracks interpersonal dynamics (type, disposition).
 * For places: tracks awareness and visit state (via familiarity thresholds).
 *
 * Familiarity thresholds:
 * - 0-19: Stranger (characters) / Unaware (places)
 * - 20-49: Knows name (characters) / Heard of but not visited (places)
 * - 50+: Well-known (characters) / Has visited (places)
 */
export interface CharacterRelationship {
  /** The entity ID this relationship is with (CHAR_xxx or PLACE_xxx) */
  targetId: string;
  /** Nature of the connection (characters only, null for places) */
  type: RelationshipType | null;
  /** Sentiment score: -100 (hatred) to +100 (devotion). Only meaningful for characters. */
  disposition: number | null;
  /** How well they know this entity: 0-100. >= 20 = aware/known, >= 50 = visited (places) */
  familiarity: number;
  /** Brief note about this relationship or how they know this place */
  context: string | null;
  /** True if place needs to be generated (places only, awaiting lazy generation) */
  pendingGeneration: boolean;
}

