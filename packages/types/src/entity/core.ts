/** Marker category for minimap rendering and visibility toggling. */
export type MinimapMarkerType = 'player' | 'npc' | 'place' | 'exit' | 'object';

/** A marker on the minimap representing an entity in the place. */
export interface MinimapMarker {
  id: string;
  label: string;
  /** Normalized position (0-1) within the place */
  x: number;
  /** Normalized position (0-1) within the place */
  y: number;
  /** Marker category for visual styling and visibility toggling */
  type: MinimapMarkerType;
  /** Purpose or interaction type for future sub-filtering (e.g. "vessel_helm", "exit"). Null if not applicable. */
  subtype: string | null;
}

/** Complete data for rendering a minimap. Returned by the minimap endpoint. */
export interface MinimapData {
  /** Current place name */
  placeName: string;
  /** Current place ID */
  placeId: string;
  /** Parent place ID (for zoom-out), null if root */
  parentId: string | null;
  /** Place environment type */
  environment: string;
  /** Player position normalized 0-1, null if not positionable */
  player: { x: number; y: number } | null;
  /** Markers for child places and exits, all normalized 0-1 */
  markers: MinimapMarker[];
  /** Weather condition, null when environment has no atmospheric weather */
  weather: string | null;
  /** Season name, null if not applicable */
  season: string | null;
  /** Temperature in degrees, null if not applicable */
  temperature: number | null;
  /** Temperature band, null if not applicable */
  temperatureBand: string | null;
  /** Formatted game time string (e.g., "14:30"), null if no calendar */
  time: string | null;
  /** Tilemap data for canvas rendering, null if no layout */
  tilemap: TilemapData | null;
}

/**
 * A feature layer entry for a race or sprite archetype.
 * Describes one optional visual feature (ears, nose, eyes, facial hair, etc.)
 * with available styles, generation probability, and player-selection rules.
 *
 * The sprite helper iterates these generically — it doesn't hardcode knowledge
 * of specific feature types. Adding a new feature type (wings, tails, horns)
 * requires only adding an entry here plus assets in the manifest.
 */
export interface RaceFeatureLayer {
  /** Manifest layer type (e.g., "ears", "nose", "eyes", or any future layer). */
  layerType: string;
  /** Available style IDs to choose from (e.g., ["elvenears"] or ["buttonnose", "bignose"]). */
  styles: string[];
  /** Probability 0-1 that this feature is included during auto-generation (1.0 = always). */
  chance: number;
  /** If true, exposed in the character creator for manual selection. */
  playerSelectable: boolean;
}

/** Non-empty array helper type. */
export type NonEmptyArray<T> = [T, ...T[]];

/**
 * Sprite generation hints for a race.
 * Used to map races to appropriate LPC sprite assets.
 */
export type RaceSpriteHints = HumanoidRaceSpriteHints | NonHumanoidRaceSpriteHints;

/** Sprite hints for races with humanoid body support. */
export interface HumanoidRaceSpriteHints {
  /** True for humanoid races that render with LPC body/head layers. */
  humanoidBody: true;
  /** Sprite archetype ID from the archetype registry (e.g., 'human', 'orc', 'wolf'). */
  spriteArchetype: string;
  /** Default skin color when generating characters of this race (v3 SkinColor value). */
  defaultSkinColor: string;
  /** Explicit allowed skin colors for this race (non-empty). */
  allowedSkinColors: NonEmptyArray<string>;
  /** Allowed eye colors for this race (empty/null = all allowed). */
  allowedEyeColors: string[] | null;
  /** Allowed hair colors for this race (null = all allowed from HAIR_COLORS). */
  allowedHairColors: string[] | null;
  /** Visual scale multiplier for this race (default 1.0). Composes with per-layout characterScale. */
  spriteScale: number;
  /** Override archetype featureLayers for this race (null = use archetype defaults). */
  featureLayers: RaceFeatureLayer[] | null;
}

/** Sprite hints for non-humanoid races (portrait-only/no body sprite). */
export interface NonHumanoidRaceSpriteHints {
  /** False for non-humanoid races that do not use LPC body/head sprites. */
  humanoidBody: false;
  /** Non-humanoid races do not use sprite archetypes. */
  spriteArchetype: null;
  /** Non-humanoid races do not use skin tint defaults. */
  defaultSkinColor: null;
  /** Non-humanoid races do not use skin tint palettes. */
  allowedSkinColors: null;
  /** Allowed eye colors for this race (empty/null = all allowed). */
  allowedEyeColors: string[] | null;
  /** Allowed hair colors for this race (null = all allowed from HAIR_COLORS). */
  allowedHairColors: string[] | null;
  /** Visual scale multiplier for this race (default 1.0). */
  spriteScale: number;
  /** Override archetype featureLayers for this race (null = use archetype defaults). */
  featureLayers: RaceFeatureLayer[] | null;
}

export interface RaceDefinition {
  id: string;
  label: string;
  description: string;
  rarity: 'rare' | 'uncommon' | 'common';
  /** Sprite generation hints for character appearance. */
  spriteHints: RaceSpriteHints | null;
}
